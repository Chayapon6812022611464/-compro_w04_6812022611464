#include <stdio.h>

int main() { // int มีหน้าที่เป็น Keyword, main ทำหน้าที่เป็น, () มีหน้าที่เป็น Operator
    printf("Hello,\n"); // printf มีหน้าที่เป็น Identifier, Hello,\n มีหน้าที่เป็น Constant, ; มีหน้าที่เป็น Operator
    printf("C Token!\n"); // printf มีหน้าที่เป็น Identifier, C Token! มีหน้าที่เป็น Constant, ; มีหน้าที่เป็น Operator

    return 0;// return มีหน้าที่เป็น Keyword, 0 มีหน้าที่เป็น Constant, ; มีหน้าที่เป็น Operator
}